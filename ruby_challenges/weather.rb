  puts "It's sunny out, go nude!"
when 'cloudy'
  puts "It's cloudy out, wear some damn pants!"
when 'foggy'
  puts "It's foggy out, wear night googles."
when 'rainy'
  puts "It's rainy out, hoodies up."
else
  puts "Sorry the planet is dead, no need for clothing anymore."
end
